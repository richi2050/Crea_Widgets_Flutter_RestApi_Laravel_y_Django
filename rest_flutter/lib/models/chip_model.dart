class ChipModel {
  String colorIcon;
  String colorBg;
  String color;
  String type;
  String icon;

  ChipModel.constructor(
      String colorIcon, String colorBg, String color, String type) {
    this.colorIcon = colorIcon;
    this.colorBg = colorBg;
    this.color = color;
    this.type = type;
  }
}
